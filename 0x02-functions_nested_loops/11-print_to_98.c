#include "main.h"
/**
 * print_to_98 - check main
 * @n: an input integer
 * Description: A function that prints
 * natural numbers from n to 98
 * RETURN: VOID
 */
void print_to_98(int n)
{
	int  n, i;

	if (i == 99)
	{
		for (; n < i; n++)
		{
			if ((n <= 0) || (n > 0))
			{
				_putchar(n);
				_putchar(',');
				_putchar(' ');
			}
		}
	}
}
